# Define two complex numbers
complex1 = complex(3, 4)  # 3 + 4i
complex2 = complex(1, 2)  # 1 + 2i

# Addition of two complex numbers
addition = complex1 + complex2

# Subtraction of two complex numbers
subtraction = complex1 - complex2

# Multiplication of two complex numbers
multiplication = complex1 * complex2

# Conjugate of the first complex number
conjugate1 = complex1.conjugate()

# Conjugate of the second complex number
conjugate2 = complex2.conjugate()

# Display results
print(f"Complex Number 1: {complex1}")
print(f"Complex Number 2: {complex2}")
print(f"Addition: {addition}")
print(f"Subtraction: {subtraction}")
print(f"Multiplication: {multiplication}")
print(f"Conjugate of Complex Number 1: {conjugate1}")
print(f"Conjugate of Complex Number 2: {conjugate2}")
